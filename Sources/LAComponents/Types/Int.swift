import SwiftUI

public extension Int {

  /// Cast an *Int* as a *Double*.
  var castDouble: Double {
    return Double(self)
  }

  /// Cast an *Int* as a *Float*.
  var castFloat: Float {
    return Float(self)
  }

  /// Cast an *Int* as a *CGFloat*.
  var castCGFloat: CGFloat {
    return CGFloat(self)
  }
}
